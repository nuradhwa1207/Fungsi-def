# Contoh penggunaan fungsi bawaan untuk manipulasi string
text = "Hello , World !" 
print(len(text)) # Menghitung panjang string
print(text.upper()) # Mengubah string menjadi huruf kapital print(text.replace("Hello", "Hi")) 
print(text.replace("Helo", "Hi"))# Mengganti kata dalam string

#Contoh penggunaan fungsi bawaan untuk operasi matematika
numbers =  [1, 2, 3, 4, 5]
print(sum(numbers)) # Menghitung jumlah elemen dalam list print(max(numbers)) 
print(max(numbers))#Mencari nilai maksimum dalam list
print(min(numbers)) # Mencari nilai minimum dalam list